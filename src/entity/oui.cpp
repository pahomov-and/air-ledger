// Build a 24-bit OUI key from the first 3 octets of a MAC address
static inline uint32_t oui_key(uint8_t a, uint8_t b, uint8_t c) {
    return (static_cast<uint32_t>(a) << 16)
         | (static_cast<uint32_t>(b) << 8)
         |  static_cast<uint32_t>(c);
}

